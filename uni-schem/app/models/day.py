from enum import Enum


class Day(Enum):
    """
    Enum representing the days of the week.

    Members:
        monday (int): Monday (1)
        tuesday (int): Tuesday (2)
        wednesday (int): Wednesday (3)
        thursday (int): Thursday (4)
        friday (int): Friday (5)
        saturday (int): Saturday (6)
    """

    MONDAY = 1
    TUESDAY = 2
    WEDNESDAY = 3
    THURSDAY = 4
    FRIDAY = 5
    SATURDAY = 6
